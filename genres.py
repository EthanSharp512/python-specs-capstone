#seeds genres
def load_genres():
    
    Genre.query.delete()

    for i in genre_data:

        genre = Genre(i)

        db.session.add(genre)
    
    db.session.commit()

#seeds subgenres
def load_subgenres():
    """load subgenres"""

    for list in subgenre_data:
        
        genre_id = list[0]
        sub_genre = list[1]

        subgenre = Subgenre(genre_id=genre_id, sub_genre=sub_genre)

        db.session.add(subgenre)

    db.session.commit()

